#include "ft_printf.h"

int	mz_s_printer(char *p)
{
	unsigned int	i;

	i = 0;
	if (!p)
	{
		write(1, "(null)", 6);
		return (5);
	}
	while (p[i])
	{
		write(1, &p[i], 1);
		i++;
	}
	return (i - 1);
}
